/*

Realizá un programa que muestre todos los números enteros del 1 al 5, y luego los mismos
números pero en orden inverso.

*/

console.log ("*****FOR 1*****")

for (let i = 0; i <= 5; i++) { // i = i + 1 
    console.log (i);    
}

console.log ("*****FOR REVERSE*****")

for (let i = 5; i >= 1; i = i - 1 ) { // i--
    console.log (i);    
}